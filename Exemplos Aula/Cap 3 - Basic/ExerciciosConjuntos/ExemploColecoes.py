conjunto= {"Goku", "Vegeta","Trunks", "Gohan"} # criando um conjunto
print(conjunto) # mostrando os elementos, por ser um conjunto ele sai em ordem aleatoria

conjunto.add("Bulma") # adicionando os elementos em um conjunto
conjunto.add("Goku") # em um conjunto, nao se permite elementos iguais
print(conjunto) # mostrando os novos elementos do conjunto

conjunto.remove("Trunks") # tira o elemento do conjunto
print(conjunto) # mostra os novos elementos do conjunto


